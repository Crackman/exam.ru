                    Основной частью фонетики является учение о звуках. Звуки речи – это
                    минимальные, далее не разложимые фонетические единицы.
                </p>
                <p>
                    Фонетику иногда так и определяют: «Фонетика- это наука о звуках речи»
                    (В.А.Богородицкий. Общий курс русской грамматики. М-Л., 1935, с. 12).
                </p>
