                    Такое определение надо признать неполным и недостаточным, т.к.
                    фонетика изучает не только звуки речи, а также и все звуковые средства
                    языка, например, слоги, фонетические слова, такты, фразы, паузы,
                    интонации. Поэтому общее определение должно быть широким.
                    Фонетика – это наука о звуковой стороне языка, изучающая способы
                    образования звуков речи, их отличительные признаки, их изменение в
                    речевом потоке, их роль в функционировании языка как средства общения
                    людей. (Скрипник Я.Н., Смоленская Т.М. Фонетика современного русского
                    языка, 2010).
                </p>

